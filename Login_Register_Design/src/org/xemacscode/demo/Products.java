    private void bt_AddMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_bt_AddMouseClicked
        if (txt_ProdId.getText().trim().isEmpty() && txt_prodName.getText().trim().isEmpty() && txt_Price.getText().trim().isEmpty()) {
            lb_ProId1.setText("Product ID is Empty");
            lb_Name.setText("Product Name is Empty");
            lb_Quanlt.setText("Quanlity is Empty");
            lb_Price1.setText("Price is Empty");
            //JOptionPane.showMessageDialog(this, "Missing Information");
        } else if (txt_ProdId.getText().trim().isEmpty()) {
            lb_ProId1.setText("Product ID is Empty");
        } else if (txt_prodName.getText().trim().isEmpty()) {
            lb_Name.setText("Product Name is Empty");

        } else if (txt_Price.getText().trim().isEmpty()) {
            lb_Price1.setText("Price is Empty");
        } else {

            try {
                //  Connection Con = DBConnection.connectDB();
                Connection Con = DriverManager.getConnection("jdbc:mysql://localhost:/demo", "root", "");
                PreparedStatement add = Con.prepareStatement("insert into PRODUCTB values(?,?,?,?)");
                add.setInt(1, Integer.valueOf(txt_ProdId.getText()));
                add.setString(2, txt_prodName.getText());
                // add.setString(3, txt_Quanlt.getText());
                add.setString(3, txt_Price.getText());
                add.setString(4, cb_Cate.getSelectedItem().toString());
                int row = add.executeUpdate();
                JOptionPane.showMessageDialog(this, "Product Added Successfully");
                showData();
                Con.close();

            } catch (Exception e) {
                e.printStackTrace();
            }
        }
    }//GEN-LAST:event_bt_AddMouseClicked

