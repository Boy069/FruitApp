    public void EditRecord() {

        String prodid = txt_ProdId.getText();
        String prodname = txt_prodName.getText();
        //String proquanlt = txt_Quanlt.getText();
        String price = txt_Price.getText();
        String prodcate = cb_Cate.getSelectedItem().toString();

        String sql = "update PRODUCTB set "
                + "ProdName='" + prodname + "',"
                //+ "ProdQty='" + proquanlt + "',"
                + "ProdPrice='" + price + "' ,"
                + "ProdCat='" + prodcate + "' "
                + "where ProdId='" + prodid + "'";
        try {
            Connection c = DBConnection.connectDB();
            Statement stm = c.createStatement();
            if (stm.executeUpdate(sql) != -1) {
                JOptionPane.showMessageDialog(this, "Product Updated");
                showData();
                clearTextBox();
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

