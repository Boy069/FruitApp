    private void tb_ProductMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tb_ProductMouseClicked
        int select = tb_Product.getSelectedRow();
        txt_ProdId.setText(tb_Product.getValueAt(select, 0).toString());
        txt_prodName.setText(tb_Product.getValueAt(select, 1).toString());
        //txt_Quanlt.setText(tb_Product.getValueAt(select, 2).toString());
        txt_Price.setText(tb_Product.getValueAt(select, 2).toString());
        String adName = tb_Product.getValueAt(select, 3).toString();
        for (int i = 0; i < cb_Cate.getItemCount(); i++) {
            if (adName.equals(cb_Cate.getItemAt(i))) {
                cb_Cate.setSelectedIndex(i);
            }

        }
    }//GEN-LAST:event_tb_ProductMouseClicked

    private void bt_DeleteMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_bt_DeleteMouseClicked
        if (txt_ProdId.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Enter The Product To Deleted");
        } else {
            try {
                Connection c = DBConnection.connectDB();
                String delId = txt_ProdId.getText();
                String sql = "delete from PRODUCTB where ProdId='" + delId + "'";
                Statement stm = c.createStatement();
                if (stm.executeUpdate(sql) != -1) {
                    JOptionPane.showMessageDialog(this, "Product Deleted Succesfully", "recording results",
                            JOptionPane.INFORMATION_MESSAGE);
                    clearTextBox();
                    showData();
                }
            } catch (Exception e) {
                e.printStackTrace();
            }
        }
    }//GEN-LAST:event_bt_DeleteMouseClicked

