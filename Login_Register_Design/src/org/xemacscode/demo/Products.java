    public void clearRow() {
        int row = model.getRowCount() - 1;
        while (row > -1) {
            model.removeRow(row);
            row--;
        }
    }

    public void showData() {
        clearRow();
        try {
            Connection Con = DBConnection.connectDB();
            St = Con.createStatement();
            Rs = St.executeQuery("Select * from PRODUCTB");
            int row = 0;
            while (Rs.next()) {
                model.addRow(data);
                model.setValueAt(Rs.getString(1), row, 0);
                model.setValueAt(Rs.getString(2), row, 1);
                model.setValueAt(Rs.getString(3), row, 2);
                model.setValueAt(Rs.getString(4), row, 3);

                row++;
            }

        } catch (Exception e) {
            e.printStackTrace();
        }
    }

    private void GetCat() {
        try {
            Connection Con = DBConnection.connectDB();
            St = Con.createStatement();
            Rs = St.executeQuery("Select * from CATEGORYTB");

            while (Rs.next()) {
                String Mycat = Rs.getString("CATNAME");
                cb_Cate.addItem(Mycat);
            }

        } catch (Exception e) {
            e.printStackTrace();
        }
    }

